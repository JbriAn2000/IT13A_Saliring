
package midterm;

public class SingleArray {
    public static void main(String[] args){
        char[] letters = {'A','B','C','D','E','F','G'};
        
       for(char x = 0; x < letters.length; x++) {
           System.out.print(letters [x] + " ");
       }
       System.out.println();
    }
    
}
